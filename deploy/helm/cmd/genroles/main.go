package main

import (
	"fmt"
	"log"
	"os"
	"path/filepath"
	"sort"
	"strings"

	"github.com/flightctl/flightctl/internal/auth/authz"
	"sigs.k8s.io/yaml"
)

const outputPath = "flightctl/templates/flightctl-default-user-roles.yaml"

type verbGroup struct {
	Verbs     []string
	Resources []string
}

type roleOutput struct {
	Name       string
	VerbGroups []verbGroup
}

func main() {
	resources, err := extractResources("../../api/*/*/openapi.yaml")
	if err != nil {
		log.Fatalf("Failed to extract resources from OpenAPI specs: %v", err)
	}

	resources = unionResources(resources, authz.GetSyntheticResources())

	permissions := authz.GetResourcePermissions()

	roleOrder := []string{"admin", "viewer", "operator", "installer"}

	var roles []roleOutput
	for _, role := range roleOrder {
		perms, ok := permissions[role]
		if !ok {
			log.Fatalf("Role %q not found in resource permissions", role)
		}
		expanded := expandPermissions(perms, resources)
		groups := groupByVerbs(expanded)
		roles = append(roles, roleOutput{
			Name:       role,
			VerbGroups: groups,
		})
	}

	output := renderTemplate(roles)
	if err := os.WriteFile(outputPath, []byte(output), 0644); err != nil {
		log.Fatalf("Failed to write output file %s: %v", outputPath, err)
	}

	fmt.Printf("Generated %s with %d roles\n", outputPath, len(roles))
}

// expandPermissions resolves the wildcard-based permission map into an explicit
// resource-to-verbs mapping, replicating the precedence logic from
// StaticAuthZ.CheckPermission: specific entries override the wildcard, and an
// empty verb list means explicit denial.
func expandPermissions(perms map[string][]string, allResources []string) map[string][]string {
	wildcardOps := perms["*"]

	isWildcardOps := len(wildcardOps) == 1 && wildcardOps[0] == "*"
	if isWildcardOps {
		return map[string][]string{"*": {"*"}}
	}

	result := make(map[string][]string)
	for _, resource := range allResources {
		if ops, exists := perms[resource]; exists {
			if len(ops) == 0 {
				continue
			}
			result[resource] = ops
		} else if len(wildcardOps) > 0 {
			result[resource] = wildcardOps
		}
	}

	for resource, ops := range perms {
		if resource == "*" {
			continue
		}
		if len(ops) == 0 {
			continue
		}
		if _, exists := result[resource]; !exists {
			result[resource] = ops
		}
	}

	return result
}

// groupByVerbs groups resources that share the same set of verbs into a single
// verbGroup for cleaner Helm template output.
func groupByVerbs(expanded map[string][]string) []verbGroup {
	verbSetToResources := make(map[string][]string)

	for resource, verbs := range expanded {
		sorted := make([]string, len(verbs))
		copy(sorted, verbs)
		sort.Strings(sorted)
		key := strings.Join(sorted, ",")
		verbSetToResources[key] = append(verbSetToResources[key], resource)
	}

	keys := make([]string, 0, len(verbSetToResources))
	for k := range verbSetToResources {
		keys = append(keys, k)
	}
	sort.Strings(keys)

	var groups []verbGroup
	for _, key := range keys {
		resources := verbSetToResources[key]
		sort.Strings(resources)
		groups = append(groups, verbGroup{
			Verbs:     strings.Split(key, ","),
			Resources: resources,
		})
	}
	return groups
}

func renderTemplate(roles []roleOutput) string {
	var b strings.Builder
	b.WriteString("# Code generated by genroles from internal/auth/authz/static.go. DO NOT EDIT.\n")
	b.WriteString("# To change role permissions, edit resourcePermissions in static.go and run: make generate\n")

	for i, role := range roles {
		if i > 0 {
			b.WriteString("\n---\n")
		}
		b.WriteString("apiVersion: rbac.authorization.k8s.io/v1\n")
		b.WriteString("kind: ClusterRole\n")
		b.WriteString("metadata:\n")
		b.WriteString(fmt.Sprintf("  name: flightctl-%s-{{ .Release.Namespace }}\n", role.Name))
		b.WriteString("  labels:\n")
		b.WriteString("    {{- include \"flightctl.standardLabels\" . | nindent 4 }}\n")
		b.WriteString("rules:\n")

		for _, group := range role.VerbGroups {
			b.WriteString("  - verbs:\n")
			for _, verb := range group.Verbs {
				if verb == "*" {
					b.WriteString("      - '*'\n")
				} else {
					b.WriteString(fmt.Sprintf("      - %s\n", verb))
				}
			}
			b.WriteString("    apiGroups:\n")
			b.WriteString("      - flightctl.io\n")
			b.WriteString("    resources:\n")
			for _, resource := range group.Resources {
				if resource == "*" {
					b.WriteString("      - '*'\n")
				} else {
					b.WriteString(fmt.Sprintf("      - %s\n", resource))
				}
			}
		}
	}
	return b.String()
}

func unionResources(a, b []string) []string {
	seen := make(map[string]struct{}, len(a))
	for _, r := range a {
		seen[r] = struct{}{}
	}
	for _, r := range b {
		if _, exists := seen[r]; !exists {
			a = append(a, r)
		}
	}
	sort.Strings(a)
	return a
}

// OpenAPI resource extraction

var httpMethods = []string{"get", "post", "put", "patch", "delete", "head", "options", "trace"}

type openAPISpec struct {
	Paths map[string]map[string]any `yaml:"paths"`
}

func getString(m map[string]any, key string) string {
	if m == nil {
		return ""
	}
	if v, ok := m[key].(string); ok {
		return v
	}
	return ""
}

func getMap(m map[string]any, key string) map[string]any {
	if m == nil {
		return nil
	}
	if v, ok := m[key].(map[string]any); ok {
		return v
	}
	return nil
}

func extractResources(globPattern string) ([]string, error) {
	files, err := filepath.Glob(globPattern)
	if err != nil {
		return nil, fmt.Errorf("invalid glob pattern %q: %w", globPattern, err)
	}
	if len(files) == 0 {
		return nil, fmt.Errorf("no files match pattern: %s", globPattern)
	}

	sort.Strings(files)
	resourceSet := make(map[string]struct{})

	for _, f := range files {
		resources, err := extractResourcesFromFile(f)
		if err != nil {
			return nil, fmt.Errorf("processing %s: %w", f, err)
		}
		for _, r := range resources {
			resourceSet[r] = struct{}{}
		}
	}

	sorted := make([]string, 0, len(resourceSet))
	for r := range resourceSet {
		sorted = append(sorted, r)
	}
	sort.Strings(sorted)
	return sorted, nil
}

func extractResourcesFromFile(filePath string) ([]string, error) {
	data, err := os.ReadFile(filePath)
	if err != nil {
		return nil, fmt.Errorf("read file: %w", err)
	}

	var spec openAPISpec
	if err := yaml.Unmarshal(data, &spec); err != nil {
		return nil, fmt.Errorf("parse YAML: %w", err)
	}

	resourceSet := make(map[string]struct{})
	for _, pathItem := range spec.Paths {
		pathXRes := getString(pathItem, "x-resource")

		for _, method := range httpMethods {
			opRaw, ok := pathItem[method]
			if !ok {
				continue
			}
			op, ok := opRaw.(map[string]any)
			if !ok {
				continue
			}

			rbac := getMap(op, "x-rbac")

			resource := getString(op, "x-resource")
			if resource == "" {
				resource = getString(rbac, "resource")
			}
			if resource == "" {
				resource = pathXRes
			}
			if resource != "" {
				resourceSet[resource] = struct{}{}
			}
		}

		if pathXRes != "" {
			resourceSet[pathXRes] = struct{}{}
		}
	}

	resources := make([]string, 0, len(resourceSet))
	for r := range resourceSet {
		resources = append(resources, r)
	}
	return resources, nil
}
