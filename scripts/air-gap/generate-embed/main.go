// generate-embed is the build-time manifest generator for flightctl-mirror-images.
//
// It reads the seven source data files, fully resolves the per-variant image
// and RPM lists (merging helm-chart-opts.yaml with per-variant images.yaml,
// filtering observability-only images, deduplicating), and writes a Go source
// file to stdout containing the resulting JSON-encoded manifest as a []byte
// variable that is compiled into the binary.
//
// Usage (called by "make generate-mirror-embed"):
//
//	go run ./scripts/air-gap/generate-embed/ \
//	    deploy/helm/helm-chart-opts.yaml \
//	    deploy/helm/flightctl/Chart.yaml \
//	    packaging/images/el9/images.yaml \
//	    packaging/images/el10/images.yaml \
//	    packaging/images/rhel9/images.yaml \
//	    packaging/images/rhel10/images.yaml \
//	    packaging/rpm/flightctl.spec
package main

import (
	"bufio"
	"bytes"
	"encoding/json"
	"fmt"
	"os"
	"sort"
	"strings"

	pkgmanifest "github.com/flightctl/flightctl/scripts/air-gap/pkg/manifest"
	"gopkg.in/yaml.v3"
)

// --------------------------------------------------------------------------
// Build-time YAML input types
// --------------------------------------------------------------------------

type helmChartOpts map[string]chartVariant

type chartVariant struct {
	Images map[string]imageSpec `yaml:"images"`
}

type imageSpec struct {
	Image string `yaml:"image"`
	Tag   string `yaml:"tag"`
}

type obsImages map[string]imageSpec

type chartMeta struct {
	AppVersion string `yaml:"appVersion"`
}

// --------------------------------------------------------------------------
// Constants
// --------------------------------------------------------------------------

// supportedVariants lists every variant the tool supports, in a stable order
// so the generated manifest is deterministic across runs.
var supportedVariants = []string{
	"community-el9",
	"community-el10",
	"rhem-el9",
	"rhem-el10",
}

// observabilityOnlyImages are skipped from the default bundle.
// They are mirrored separately when installing flightctl-observability.
var observabilityOnlyImages = map[string]bool{
	"grafana":    true,
	"prometheus": true,
}

// --------------------------------------------------------------------------
// main
// --------------------------------------------------------------------------

func main() {
	const nArgs = 7
	if len(os.Args)-1 != nArgs {
		fmt.Fprintf(os.Stderr, "usage: generate-embed <helm-chart-opts.yaml> <Chart.yaml> "+
			"<el9/images.yaml> <el10/images.yaml> <rhel9/images.yaml> <rhel10/images.yaml> <flightctl.spec>\n")
		fmt.Fprintf(os.Stderr, "expected %d arguments, got %d\n", nArgs, len(os.Args)-1)
		os.Exit(1)
	}

	helmChartOptsPath := os.Args[1]
	chartYAMLPath := os.Args[2]
	el9Path := os.Args[3]
	el10Path := os.Args[4]
	rhel9Path := os.Args[5]
	rhel10Path := os.Args[6]
	specPath := os.Args[7]

	// Read source files (obs files are optional — warn and continue if missing)
	helmData := mustRead(helmChartOptsPath)
	chartData := mustRead(chartYAMLPath)
	el9Data := tryRead(el9Path)
	el10Data := tryRead(el10Path)
	rhel9Data := tryRead(rhel9Path)
	rhel10Data := tryRead(rhel10Path)
	specData := mustRead(specPath)

	// Parse chart appVersion
	var meta chartMeta
	if err := yaml.Unmarshal(chartData, &meta); err != nil {
		fatalf("parse Chart.yaml: %v", err)
	}

	// Parse helm-chart-opts.yaml
	var opts helmChartOpts
	if err := yaml.Unmarshal(helmData, &opts); err != nil {
		fatalf("parse helm-chart-opts.yaml: %v", err)
	}

	// Parse RPM Requires — variant-independent
	rpms := parseRPMRequires(specData)

	// Resolve per-variant image lists
	m := pkgmanifest.Build{
		SchemaVersion: pkgmanifest.CurrentSchemaVersion,
		AppVersion:    meta.AppVersion,
		Variants:      make(map[string]pkgmanifest.Variant, len(supportedVariants)),
	}

	obsDataFor := func(variant string) []byte {
		switch {
		case strings.Contains(variant, "rhem") && strings.Contains(variant, "el10"):
			return rhel10Data
		case strings.Contains(variant, "rhem"):
			return rhel9Data
		case strings.Contains(variant, "el10"):
			return el10Data
		default:
			return el9Data
		}
	}

	for _, variant := range supportedVariants {
		cv, ok := opts[variant]
		if !ok {
			warnf("variant %q not found in helm-chart-opts.yaml — skipping", variant)
			continue
		}
		images := resolveImages(cv, obsDataFor(variant), variant)
		m.Variants[variant] = pkgmanifest.Variant{
			Images: images,
			RPMs:   rpms,
		}
		infof("%s: %d images, %d RPM packages", variant, len(images), len(rpms))
	}

	// Marshal to pretty JSON
	jsonData, err := json.MarshalIndent(m, "", "  ")
	if err != nil {
		fatalf("marshal manifest: %v", err)
	}

	// Emit Go source file
	fmt.Println("// Code generated by scripts/air-gap/generate-embed. DO NOT EDIT.")
	fmt.Println("// Run \"make generate-mirror-embed\" to regenerate.")
	fmt.Println("//")
	fmt.Println("// embeddedBuildManifest contains the fully-resolved image and RPM lists for")
	fmt.Println("// all supported variants.  It is produced at build time from source files in")
	fmt.Println("// the flightctl repository so the binary is self-contained when installed via RPM.")
	fmt.Println("//")
	fmt.Println("// Known limitations:")
	fmt.Println("//   - The embedded image set is version-pinned to build time.  --tag-override")
	fmt.Println("//     changes which tag is mirrored but cannot change which images are included.")
	fmt.Println("//   - To pre-stage images for an upcoming upgrade before it is installed,")
	fmt.Println("//     either install the target version's flightctl-cli RPM, or set")
	fmt.Println("//     MIRROR_MANIFEST to a manifest produced from that version's source tree.")
	fmt.Println("//   - Custom image configurations require a custom manifest (MIRROR_MANIFEST).")
	fmt.Println("package main")
	fmt.Println()
	fmt.Printf("var embeddedBuildManifest = []byte(`\n%s\n`)\n", jsonData)
}

// --------------------------------------------------------------------------
// Image resolution
// --------------------------------------------------------------------------

// resolveImages builds the deduplicated, sorted image list for one variant by
// merging helm-chart-opts.yaml images with the variant-appropriate images.yaml.
// Observability-only images are excluded from the default list.
func resolveImages(cv chartVariant, obsData []byte, variant string) []pkgmanifest.Image {
	seen := make(map[string]bool)
	var images []pkgmanifest.Image

	// Helm chart images
	for _, spec := range cv.Images {
		if spec.Image == "" {
			continue
		}
		ref := normalizeDockerImage(spec.Image)
		tag := canonicalTag(spec.Tag)
		if !seen[ref] {
			seen[ref] = true
			images = append(images, pkgmanifest.Image{Ref: ref, Tag: tag})
		}
	}

	// Observability / RPM-only images from images.yaml
	if len(obsData) > 0 {
		var obs obsImages
		if err := yaml.Unmarshal(obsData, &obs); err != nil {
			warnf("parse images.yaml for %s: %v — skipping", variant, err)
		} else {
			for key, spec := range obs {
				if observabilityOnlyImages[key] {
					infof("%s: skipping observability-only image %q (mirror separately for flightctl-observability)", variant, key)
					continue
				}
				if spec.Image == "" {
					warnf("%s: image entry %q has no image field — skipping", variant, key)
					continue
				}
				ref := normalizeDockerImage(spec.Image)
				tag := canonicalTag(spec.Tag)
				if !strings.Contains(variant, "rhem") && strings.HasPrefix(ref, "registry.redhat.io") {
					warnf("%s: community variant references registry.redhat.io image %q — requires downstream registry access", variant, ref)
				}
				if !seen[ref] {
					seen[ref] = true
					images = append(images, pkgmanifest.Image{Ref: ref, Tag: tag})
				}
			}
		}
	}

	// Sort by Ref for deterministic output
	sort.Slice(images, func(i, j int) bool {
		return images[i].Ref < images[j].Ref
	})

	return images
}

// canonicalTag converts "latest" or "" to "" (use effectiveTag at runtime)
// and returns any other tag unchanged (fixed/pinned at build time).
func canonicalTag(tag string) string {
	if tag == "" || tag == "latest" {
		return ""
	}
	return tag
}

// normalizeDockerImage expands Docker Hub official image names to their
// canonical "docker.io/library/<name>" form so namespace paths are preserved
// in the destination registry.
func normalizeDockerImage(image string) string {
	parts := strings.SplitN(image, "/", 2)
	if len(parts) == 2 && parts[0] == "docker.io" && !strings.Contains(parts[1], "/") {
		return "docker.io/library/" + parts[1]
	}
	return image
}

// --------------------------------------------------------------------------
// RPM spec parsing
// --------------------------------------------------------------------------

// parseRPMRequires scans the RPM spec data for bare "Requires:" lines and
// returns a sorted, deduplicated list of package names.
func parseRPMRequires(data []byte) []string {
	seen := make(map[string]struct{})
	scanner := bufio.NewScanner(bytes.NewReader(data))
	for scanner.Scan() {
		line := scanner.Text()
		if !strings.HasPrefix(line, "Requires:") {
			continue
		}
		fields := strings.Fields(line)
		if len(fields) < 2 {
			continue
		}
		pkg := fields[1]
		if strings.HasPrefix(pkg, "/") {
			continue // file-path dependency
		}
		pkg = strings.Split(pkg, "=")[0]
		pkg = strings.TrimRight(pkg, " \t<>!")
		pkg = strings.TrimSpace(pkg)
		if pkg != "" {
			seen[pkg] = struct{}{}
		}
	}

	if err := scanner.Err(); err != nil {
		fatalf("scanning RPM spec: %v", err)
	}

	result := make([]string, 0, len(seen))
	for pkg := range seen {
		result = append(result, pkg)
	}
	sort.Strings(result)
	return result
}

// --------------------------------------------------------------------------
// Helpers
// --------------------------------------------------------------------------

func mustRead(path string) []byte {
	data, err := os.ReadFile(path)
	if err != nil {
		fatalf("read %s: %v", path, err)
	}
	return data
}

func tryRead(path string) []byte {
	data, err := os.ReadFile(path)
	if err != nil {
		warnf("read %s: %v — continuing without it", path, err)
		return nil
	}
	return data
}

func infof(format string, args ...any) {
	fmt.Fprintf(os.Stderr, "[INFO]  "+format+"\n", args...)
}

func warnf(format string, args ...any) {
	fmt.Fprintf(os.Stderr, "[WARN]  "+format+"\n", args...)
}

func fatalf(format string, args ...any) {
	fmt.Fprintf(os.Stderr, "[ERROR] "+format+"\n", args...)
	os.Exit(1)
}
